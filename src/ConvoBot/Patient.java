package ConvoBot;

public class Patient {
	
	private int age;
	private String name;
	private String occupation;
	

	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}
	
	public void setOccupation(String occupation) {
		this.occupation = occupation;
	}
	
}
